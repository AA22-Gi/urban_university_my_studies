from math import pi, sqrt


class Figure:
    sides_count = 0

    def __init__(self, color: tuple = (0, 0, 0), *sides):
        self.filled = False
        self.__color = list(color)
        if len(sides) == self.sides_count:
            self.__sides = list(sides)
        else:
            self.__sides = [sides[0]] * self.sides_count

    def get_color(self):
        return self.__color

    def __is_valid_color(self, r, g, b):
        for color in r, g, b:
            if not (isinstance(color, int) and 0 <= color < 256):
                return False
        return True

    def set_color(self, r, g, b):
        if self.__is_valid_color(r, g, b):
            self.__color = [r, g, b]

    def get_sides(self):
        return self.__sides

    def __is_valid_sides(self, *sides):
        sides = list(*sides)
        if len(sides) == len(self.__sides):
            for side in sides:
                if not isinstance(side, int) or side <= 0:
                    return False
            return True

    def set_sides(self, *new_sides):
        if self.__is_valid_sides(new_sides):
            self.__sides = list(new_sides)

    def __len__(self):
        return sum(self.__sides)


class Circle(Figure):
    sides_count = 1

    def __init__(self, color: tuple = (0, 0, 0), *sides):
        super().__init__(color, *sides)
        self.__radius = self.__len__() / (2 * pi)

    def get_radius(self):
        return self.__radius

    def get_diameter(self):
        return self.__radius * 2

    def get_square(self):
        return pi * self.__radius ** 2


class Triangle(Figure):
    sides_count = 3

    def __init__(self, color: tuple = (0, 0, 0), *sides):
        super().__init__(color, *sides)
        self.__height = self.__calculate_height()

    def __calculate_height(self):
        square = self.get_square()
        return square * 2 / self.get_sides()[0]

    def get_square(self):
        semiperimeter = sum(self.get_sides()) / 2  # –Ω–∞—Ö–æ–¥–∏–º –ø–æ–ª—É–ø–µ—Ä–∏–º–µ—Ç—Ä —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞
